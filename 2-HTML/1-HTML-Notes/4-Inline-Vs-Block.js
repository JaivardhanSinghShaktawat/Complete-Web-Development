/* 
---------------------------------- Inline Vs Block Elements ------------------------

--> What are inline elements ? 
    Inline elements takes only the required width according to their content.
    They allow elements to come in their left , right , top and bottom. 
    They don't take 100% width of their parent element. 

    Ex : span , a , em , strong , img , a , q , s , del etc.

--> What are block elements ? 
    Block level elements takes 100% width of their parent element. 
    They don't allow other elements to come in their left and right side.
    They take 100% width of their parent elements even if there content is small.

    Ex : div , section , footer , headings , p , pre , blockquote , figure , main , 
    header , nav etc.

*/