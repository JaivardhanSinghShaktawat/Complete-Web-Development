/* 
---------------------------- Absolute Vs Relative Path ------------------------------

--> Absolute Path : It is the complete path from the root till the file or directory
    that you want. 

    Absolute path can be used to link external server web pages or external 
    images or other resources.
    
--> Relative Path : It is the shortest path.It is relative to the current file or 
    folder.

    Relative path is generally used when we want to provide the path to some 
    resource that is present on the same server. 

--> Note : Always use relative path while working with HTML , CSS and JavaScript.This is 
    because the website when hosted is deployed on the server.On the server the 
    absolute path will be different but the relative path won't. 
    
    Both absolute path and relative path have advantages. 

*/
