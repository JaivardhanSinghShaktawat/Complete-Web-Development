/* 
--------------------------------- Frontend Vs Backend ------------------------------

--> Frontend : Anything that happens on the client side or browser is called as 
    frontend side. 

    Backend : Anything that happens on the server is called as backend side. 

--> Frontend Development : The development of code that is executed on the client 
    side or browser is called as frontend development. 
    Developers who write such code are called as frontend developers.

    Backend Development : The development of code that is executed on the server side
    is called as backend development. 
    Developers who write such code are called as backend developers.

--> What are static Websites ? 
    These are the web pages , fonts , css files that are stored on the server and
    are send as it is to the client wihtout any changes as a response. 

    Ex : any single html file with/without css file is called as static website. 

--> What are dynamic Websites ? 
    These are those websites that are created and assembled on the server and are
    send to the client as a response.
    For each and every new request , a new web page is created on the server and than
    is send as the response. 
    Client side render websites can also be dynamic.The main difference is that 
    in dynamic websites the data changes rapidly but in static no data changes.

--> What is Client Side Render Websites ? 
    These are Single Page Applications.A single html , css and js file is sent from 
    the server to the client after which javascript renders different views 
    for different urls. 

--> What is Server Side Render Websites ? 
    These are those websites that are created and assembled on the server and are
    send as the response to the client.

*/
