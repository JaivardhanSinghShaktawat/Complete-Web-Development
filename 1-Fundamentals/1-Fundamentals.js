/* 
---------------------------------- Fundamentals ------------------------------------

--> What is Client ? 
    Anyone who makes a request to the server is called as client.Generally , the 
    browser is called as client.

--> What is a Server ? 
    A server is program that is running on a computer that is connected to the 
    internet , has IP address and is capable of sending outgoing responses and receiving incoming
    requests.

--> What is Network ? 
    Computers connected together is called as network. 

--> What is Internet ? 
    It is a global network of networks.
    Computers uses TCP/IP protocols to communicate with each other.

--> What is IP address ? 
    It is the address of the computer on the internet.
    
--> What is MAC address ? 
    It is the address of the hardware of a computer.

--> What is URL ? 
    It stands for uniform resource locator.It is used to identify a resource on the
    internet.

--> What is Web ? 
    It is a part of the internet that allows us to share and access documents over
    the internet. 
    It uses HTTP and HTTPS protocols.

--> What is a browser ? 
    A browser is a program that parse HTML , CSS and JavaScript and renders them
    into a web page. 
    A browser allows us to communicate with other machines or computers.

*/
